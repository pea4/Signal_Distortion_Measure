/**
  * @brief  This function handles PPP interrupt request.
  * @param  None
  * @retval None
  */
extern int receive_flg;
extern uint8_t rx_buf[30];
extern uint16_t num;

void USART2_IRQHandler(void)
{	
	if(USART_GetITStatus(DEBUG_USART2,USART_IT_RXNE)!=RESET)
	{	
		
		rx_buf[num++] = USART_ReceiveData(DEBUG_USART2);
		if((rx_buf[0] == 0xF0 ) && (rx_buf[18]==0x0D ) && (rx_buf[19]==0x0F) && (rx_buf[20]==0x0F)&& num==21)
		{
			receive_flg=1;
			USART_ClearITPendingBit(USART1,USART_IT_RXNE);
		}
	}
}

